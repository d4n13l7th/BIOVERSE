ALTER TABLE public.materi
    ADD COLUMN IF NOT EXISTS connecting_fact TEXT,
    ADD COLUMN IF NOT EXISTS vtt_url VARCHAR(255),
    ADD COLUMN IF NOT EXISTS sync_timestamps JSONB,
